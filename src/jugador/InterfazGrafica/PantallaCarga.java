    private void cargarListaEnBoton()
    {
        ArrayList <String> listaNick = new ArrayList();
        listaNick = Comunicacion.obtenerListaNick().getListaNick();
            for(String nick: listaNick)
                jCBListaNickJugador1.addItem(nick);

            for(String nick: listaNick)
                jCBListaNickJugador2.addItem(nick);

    }

    private void jBContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBContinuarActionPerformed

        if ((jPFPasswordJugador1.getPassword().length==0) || (jPFPasswordJugador2.getPassword().length==0))
             JOptionPane.showMessageDialog(null,"Deben rellenar todos los campos para lograr cargar una partida","Advertencia",2,null);
        else
        {
           String mensaje = Comunicacion.cargarPartida(jCBListaNickJugador1, jPFPasswordJugador1.getPassword(),
                                          jCBListaNickJugador2, jPFPasswordJugador2.getPassword());
            if(mensaje.equals(""))
            {
                JOptionPane.showMessageDialog(null,"Se cargo Exit√≥samente la partida","iBattle",2,null);
                this.setVisible(false);
                PantallaJuego pantallaJuego = new PantallaJuego();
                this.setVisible(false);
                pantallaJuego.setLocationRelativeTo(null);
                pantallaJuego.setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null,mensaje,"Advertencia",1,null);
                jPFPasswordJugador1.setText(null);
                jPFPasswordJugador2.setText(null);
            }
            
        }
    }//GEN-LAST:event_jBContinuarActionPerformed

